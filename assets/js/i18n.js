(function () {
  "use strict";
  const KEY = "hk_lang";
  const getLang = () => {
    const s = localStorage.getItem(KEY);
    return HK_LANGS.includes(s) ? s : "tr";
  };
  let lang = getLang();

  window.HK_LANG = lang;
  window.hkT = (k) => (HK_I18N[lang] && HK_I18N[lang][k] != null)
    ? HK_I18N[lang][k]
    : (HK_I18N.tr[k] != null ? HK_I18N.tr[k] : k);

  function setMultiline(el, text) {
    el.replaceChildren();
    String(text).split("\n").forEach((line, i) => {
      if (i > 0) el.appendChild(document.createElement("br"));
      el.appendChild(document.createTextNode(line));
    });
  }

  function apply() {
    document.documentElement.lang = lang;
    document.querySelectorAll("[data-i18n]").forEach((el) => {
      el.textContent = window.hkT(el.getAttribute("data-i18n"));
    });
    document.querySelectorAll("[data-i18n-br]").forEach((el) => {
      setMultiline(el, window.hkT(el.getAttribute("data-i18n-br")));
    });
    document.querySelectorAll("[data-i18n-ph]").forEach((el) => {
      el.setAttribute("placeholder", window.hkT(el.getAttribute("data-i18n-ph")));
    });
    document.querySelectorAll("[data-i18n-title]").forEach((el) => {
      el.setAttribute("title", window.hkT(el.getAttribute("data-i18n-title")));
    });
    document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
      el.setAttribute("aria-label", window.hkT(el.getAttribute("data-i18n-aria")));
    });
    // Dil düğmelerinin aktif durumu
    document.querySelectorAll("[data-lang]").forEach((b) => {
      b.classList.toggle("on", b.getAttribute("data-lang") === lang);
    });
  }

  window.hkSetLang = function (l) {
    if (!HK_LANGS.includes(l)) return;
    lang = l;
    window.HK_LANG = l;
    localStorage.setItem(KEY, l);
    apply();
    // Dinamik içeriği (ürün/haber/doküman) yeniden çiz
    if (typeof window.hkRenderDynamic === "function") window.hkRenderDynamic();
    document.dispatchEvent(new CustomEvent("hk:langchange", { detail: { lang: l } }));
  };

  // Sayfa yüklenince uygula
  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", apply);
  } else {
    apply();
  }
  window.hkApplyI18n = apply;
})();
